public class Rainfall {

    private String time;
    private String aveValue;
    private String minValue;
    private String maxValue;
    private String referValue;

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getAveValue() {
        return aveValue;
    }

    public void setAveValue(String aveValue) {
        this.aveValue = aveValue;
    }

    public String getMinValue() {
        return minValue;
    }

    public void setMinValue(String minValue) {
        this.minValue = minValue;
    }

    public String getMaxValue() {
        return maxValue;
    }

    public void setMaxValue(String maxValue) {
        this.maxValue = maxValue;
    }

    public String getReferValue() {
        return referValue;
    }

    public void setReferValue(String referValue) {
        this.referValue = referValue;
    }
}
